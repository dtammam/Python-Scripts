'''
The newline Character

    - We use a special character called the "newline" to indicate when a line ends
    - We represent it as \n in strings
    - Newline is still one character - not two

	Opening Files
		- A text file can be thought of as a sequence of new lines
		- A text file has newlines at the end of each line
		- It doesn't remember the 'enter' button and isn't magic
		- A blank line is represented by \n
		- Newlines are white space! Things you don't see but exist
        - By default, print() prints out a newline for us at the end
'''
stuff = 'Hello\nWorld!'
print(stuff)

stuff = 'X\nY'
print(stuff)

print(len(stuff))