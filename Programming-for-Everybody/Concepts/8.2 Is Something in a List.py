'''
Is Something in a List?

    - Python provides two operators that let you check if an item is in a list, 'in' and 'not in'
    - These are logical operators that return True or False
    - They do not modify the list
'''
some = [1, 9, 21, 10, 16]
print(9 in some)
print(15 in some)
print(20 not in some)